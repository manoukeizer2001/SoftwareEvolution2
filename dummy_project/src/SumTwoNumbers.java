public class SumTwoNumbers {

    // Method 1: Adds two integers (Type 1 clone of AddTwoNumbers.add)
    public int sum(int a, int b) {
        return a + b;
    }

    // Method 2: Subtracts two integers (not a clone)
    public int subtract(int a, int b) {
        return a - b;
    }
}
